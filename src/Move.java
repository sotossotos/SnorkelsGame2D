
public class Move 
{
	private int xCoordinate;
	private int yCoordinate;
	public Move()
	{
		xCoordinate=0;
		yCoordinate=0;
	}
	public int getX()
	{
		return xCoordinate;
	}
	public int getY()
	{
		return yCoordinate;
	}
	public void setX(int x)
	{
		xCoordinate=x;
	}
	public void setY(int y)
	{
		yCoordinate=y;
	}
}
